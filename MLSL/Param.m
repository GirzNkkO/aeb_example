% 0.0;自車の中央を狙う  ,-0.5;自車の手前0.5mを狙い遅く出る  ,+0.5;自車の奥側0.5mを狙い早く出る
% 2.0は、MCのSim環境でチューニングした値〜PCパフォーマンスなどによって変更要！
